#include "libft.h"

void	ft_putnbrlong(long n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
		ft_putnbrlong(n / 10);
	ft_putchar((n % 10) + '0');
}
