#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		len_s1;
	int		i;

	i = 0;
	len_s1 = ft_strlen(src);
	dest = (char *)malloc(sizeof(char) * len_s1 + 1);
	if (dest == NULL)
		return (dest);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
