#include "libft.h"

int		ft_nbrlen(long int n)
{
	int res;

	res = 0;
	if (n <= 0)
	{
		n = -n;
		res++;
	}
	while (n > 0)
	{
		n /= 10;
		res++;
	}
	return (res);
}
