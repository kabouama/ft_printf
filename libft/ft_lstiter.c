#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *nextlist;

	while (lst)
	{
		nextlist = lst->next;
		(*f)(lst);
		lst = nextlist;
	}
}
