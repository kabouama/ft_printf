#include "libft.h"

int		ft_isalpha(int c)
{
	int flag;
	int i;

	flag = 1;
	i = 0;
	if (!((c >= 'a' && c <= 'z')
				|| (c >= 'A' && c <= 'Z')))
		flag = 0;
	return (flag);
}
